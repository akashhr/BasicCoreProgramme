package com.bridgelabz.basiccoreprogramme;

public class EvenOddChecker {
	public static void main(String[] args ) {
		int numberToCheck = 1;
		if(numberToCheck % 2 == 0) {
			System.out.println("The number : "+  numberToCheck + " is a even number!!!!.");
		}
		else{
			System.out.println("The number : "+ numberToCheck + " is a odd number!!!.");
		}
	}
}
